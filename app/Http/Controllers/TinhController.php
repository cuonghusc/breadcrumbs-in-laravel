     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request->name);
        $tinhthanh = TinhThanh::where('name', '=', $request->name)->first();
        if($tinhthanh == null){
            $flight = new TinhThanh;
            $flight->name = $request->name;
            $flight->save();
            return redirect()->route('create')->with('success','Thêm thành công !');
        }else{
            return redirect()->route('create')->with(['failed'=>'Thêm thất bại !','request'=>$request]);
        }
        // dd($tinhthanh);
    }

